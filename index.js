function handleLoginButtonClick() {
	$('#login_button').click(function() {
		$('#login_button').css('box-shadow', 'rgba(0, 0, 0, 0.4) 0px 0px 20px 0px');
		$('#login_form').fadeIn();
	});
}

/*--------------------------------
----------------------------------
---------------MAIN---------------
----------------------------------
--------------------------------*/

$(function() {
	$('#login_form').hide();
	handleLoginButtonClick();
});
