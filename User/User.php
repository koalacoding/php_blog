<?php
class User {

/*---------------------------------
-----------------------------------
---------------LOGIN---------------
-----------------------------------
---------------------------------*/

function login($username) {
  require($_SERVER['DOCUMENT_ROOT'] . '/yoBlog/include/session.php');
  $_SESSION['username'] = $username;
}

/*----------------------------------
------------------------------------
---------------LOGOUT---------------
------------------------------------
----------------------------------*/

function logout() {
	session_unset();
	session_destroy();
}

}
